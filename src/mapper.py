"""
mapper.py — generates compact project context maps for LLM prompts.

This is the core "killer feature" of claude-codebase-navigator:
instead of having the LLM explore your repos token-by-token via MCP,
you pre-generate a structured map and paste it into the prompt.

Typical token savings: 40–70% compared to live MCP exploration.
"""
from __future__ import annotations

import json
import os
import re
from pathlib import Path
from typing import Optional

from .config import Config
from .scanner import ProjectScanner


def _parse_docker_compose(path: Path) -> dict:
    """Extract services, host ports, and key env vars from docker-compose.yml. Pure Python."""
    result: dict[str, dict] = {}
    for fname in ["docker-compose.yml", "docker-compose.yaml"]:
        dc = path / fname
        if not dc.exists():
            continue
        text = dc.read_text(errors="ignore")
        current: Optional[str] = None
        in_ports = in_env = False

        for line in text.splitlines():
            stripped = line.strip()
            if not stripped or stripped.startswith("#"):
                continue
            indent = len(line) - len(line.lstrip())

            if indent == 2 and stripped.endswith(":") and not stripped.startswith("-"):
                name = stripped[:-1]
                skip = {"version", "services", "volumes", "networks", "configs", "secrets"}
                if name not in skip:
                    current = name
                    result.setdefault(current, {"ports": [], "env": {}})
                else:
                    current = None
                in_ports = in_env = False
                continue

            if current is None:
                continue

            if indent == 4:
                in_ports = stripped == "ports:"
                in_env = stripped in ("environment:", "env:")
                continue

            if in_ports and indent >= 6:
                raw = stripped.lstrip("- ").strip().strip("\"'")
                nums = re.findall(r'(?<!\d)(\d{2,5})(?!\d)', raw)
                if len(nums) >= 2:
                    result[current]["ports"].append({"host": nums[0], "container": nums[-1]})
                elif len(nums) == 1:
                    result[current]["ports"].append({"host": nums[0], "container": nums[0]})

            if in_env and indent >= 6:
                raw = stripped.lstrip("- ").strip()
                m = re.match(r'^([A-Za-z_][A-Za-z0-9_]*)=(.*)$', raw) \
                 or re.match(r'^([A-Za-z_][A-Za-z0-9_]*):\s*(.*)$', raw)
                if m:
                    k = m.group(1)
                    v = m.group(2).strip().strip("\"'")
                    dm = re.search(r'\$\{[A-Za-z_]+(?::?[-=])([^}]+)\}', v)
                    result[current]["env"][k] = dm.group(1) if dm else v
        break
    return result


def generate_map(project_names: list[str], catalog: dict, cfg: Config) -> str:
    """
    Build a compact, token-efficient context map for the given projects.
    Output is designed to be pasted directly into a Claude/GPT prompt.
    """
    lines = [
        "# 🗺 Project Map",
        f"*Projects: {', '.join(project_names)}*",
        "*Generated by claude-codebase-navigator — paste this into your LLM prompt*",
        "",
    ]

    for name in project_names:
        project = catalog.get(name)

        if not project and name in cfg.local_only:
            local_path = cfg.local_only[name]
            if local_path and local_path.exists():
                scanner = ProjectScanner(local_path, cfg)
                scanner.name = name
                project = scanner.scan()

        if not project:
            resolved = cfg.resolve_local_path(name)
            if resolved and resolved.exists():
                scanner = ProjectScanner(resolved, cfg)
                scanner.name = name
                project = scanner.scan()

        if not project:
            lines.append(f"## ❌ {name}")
            lines.append("*Not found in catalog or filesystem.*")
            lines.append("")
            continue

        git  = project.get("git") or {}
        tech = project.get("tech_stack", {})
        files = project.get("files", {})
        by_type = files.get("by_type", {})
        integrations = project.get("integrations", [])

        lines.append(f"## {project['status']} {name}")

        url = cfg.gitlab_url(name)
        if url:
            lines.append(f"**Remote:** {url}")

        if git.get("branch"):
            date_short = (git.get("last_commit_date") or "")[:10]
            lines.append(f"**Git:** branch `{git['branch']}` · last commit {date_short} — {git.get('last_commit_msg','')}")

        stack_parts = (tech.get("languages", []) or []) + (tech.get("frameworks", []) or [])
        if stack_parts:
            lines.append(f"**Stack:** {', '.join(stack_parts)}")

        docker = _parse_docker_compose(Path(project["path"]))
        if docker:
            lines.append("**Docker services:**")
            _KEY_ENV = {"POSTGRES_USER", "POSTGRES_DB", "DB_PORT", "DB_NAME", "API_PORT", "PORT", "CLICKHOUSE_DATABASE", "REDIS_URL"}
            for svc_name, svc in docker.items():
                port_str = ", ".join(f"{p['host']}→{p['container']}" for p in svc["ports"]) or "—"
                shown_env = {k: v for k, v in svc["env"].items() if k in _KEY_ENV or (not v.startswith("${") and v)}
                env_str = " | ".join(f"{k}={v}" for k, v in list(shown_env.items())[:5])
                env_part = f" · env: {env_str}" if env_str else ""
                lines.append(f"  - `{svc_name}` ports: {port_str}{env_part}")

        code_files = sorted(
            by_type.get("code", {}).get("files", []),
            key=lambda f: (
                0 if any(k in Path(f["path"]).name.lower() for k in ["main", "app", "server", "orchestrator", "router", "cli", "index"]) else 1,
                len(f["path"].split("/")),
            )
        )[:8]
        if code_files:
            lines.append(f"**Key files:** {', '.join(f['path'] for f in code_files)}")

        code_n = by_type.get("code", {}).get("count", 0)
        docs_n = by_type.get("docs", {}).get("count", 0)
        data_n = by_type.get("data", {}).get("count", 0)
        lines.append(f"**Files:** {code_n} code / {docs_n} docs / {data_n} data")

        relevant = [i for i in integrations if i.get("target") in project_names or i.get("source") == "manual"]
        if relevant:
            lines.append("**Connects to →**")
            for i in relevant:
                lines.append(f"  - `{i.get('target','')}` via {i.get('type','')} `{i.get('url','')}` — {i.get('data','')}")

        readme = project.get("readme", "")
        if readme:
            first = readme.split("\n\n")[0].replace("\n", " ").strip()
            first = re.sub(r'^#+\s*', '', first)
            if len(first) > 400:
                first = first[:397] + "…"
            lines.append(f"**README:** {first}")

        lines.append("")

    return "\n".join(lines)
